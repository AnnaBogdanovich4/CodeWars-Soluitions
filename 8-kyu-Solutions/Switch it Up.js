//When provided with a number between 0-9, return it in words.
// Input :: 1
// Output :: "One". 
// If your language supports it, try using a switch statement.

let switchItUp = (number) =>{
    switch (number){
        case 0:
            return number = 'Zero'
            break
        case 1:
            return number = 'One'
            break
        case 2:
            return number = 'Two'
            break
        case 3:
            return number = 'Three'
            break
        case 4:
            return number = 'Four'
            break
        case 5:
            return number = 'Five'
            break
        case 6:
            return number = 'Six'
            break
        case 7:
            return number = 'Seven'
            break
        case 8:
            return number = 'Eight'
            break
        case 9:
            return number = 'Nine'
            break
        default:
            return number = 'This is not diapazon of 0 to 9'
            break
    }
}